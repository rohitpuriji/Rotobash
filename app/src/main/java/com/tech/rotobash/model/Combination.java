public class Combination {
    private String BT;
    private String BW;
    private String AR;
    private String WK;

    public String getBT() {
        return BT;
    }

    public void setBT(String BT) {
        this.BT = BT;
    }

    public String getBW() {
        return BW;
    }

    public void setBW(String BW) {
        this.BW = BW;
    }

    public String getAR() {
        return AR;
    }

    public void setAR(String AR) {
        this.AR = AR;
    }

    public String getWK() {
        return WK;
    }

    public void setWK(String WK) {
        this.WK = WK;
    }
}
